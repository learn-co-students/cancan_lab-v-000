class UsersController < ApplicationController
  def create
    # raise params.inspect
    @user = User.new(user_params)
    if @user.save
      session[:user_id] = @user.id
      redirect_to '/notes'
    else
      render 'sessions/signup'
    end
  end

  private
    def user_params
      params.require(:user).permit(:name)
    end
end
